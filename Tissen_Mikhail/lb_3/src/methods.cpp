#include <stdio.h>
#include <math.h>
#include <stdlib.h>

extern double F(double);

// ============================================================
// Функция Round - округление числа до заданной точности Delta
// Округляет до ближайшего значения, кратного Delta
// Применяется для моделирования погрешностей вычислений функции
// ============================================================
double Round(double X, double Delta)
{
    if (Delta <= 0.0) return X;  // Без округления
    if (Delta <= 1E-9) {
        printf("Неверное задание точности округления\n");
        exit(1);
    }
    // Округление до ближайшего кратного Delta
    if (X > 0.0)
        return Delta * (long)((X / Delta) + 0.5);
    else
        return Delta * (long)((X / Delta) - 0.5);
}

// ============================================================
// Метод бисекции (половинного деления)
// Находит корень уравнения F(x) = 0 на отрезке [Left, Right]
// Delta - параметр округления значений функции (для исследования чувствительности)
// ============================================================
double BISECT(double Left, double Right, double Eps, int &N, double Delta)
{
    double E = fabs(Eps) * 2.0;      // Длина интервала для остановки
    // Округляем значения функции с точностью Delta (моделирование ошибок)
    double FLeft = Round(F(Left), Delta);
    double FRight = Round(F(Right), Delta);
    double X, Y;

    // Проверка условия теоремы Коши (разные знаки на концах)
    if (FLeft * FRight > 0.0) {
        printf("Неверное задание интервала\n");
        exit(1);
    }
    // Проверка корректности точности
    if (Eps <= 0.0) {
        printf("Неверное задание точности\n");
        exit(1);
    }

    N = 0;
    // Проверка граничных точек
    if (FLeft == 0.0) return Left;
    if (FRight == 0.0) return Right;

    // Основной цикл метода бисекции
    while ((Right - Left) >= E) {
        X = 0.5 * (Right + Left);    // Середина текущего отрезка
        // Округляем значение функции в середине (моделирование ошибок)
        Y = Round(F(X), Delta);

        if (Y == 0.0) return X;      // Точное попадание в корень

        // Выбор половины, где функция меняет знак
        if (Y * FLeft < 0.0)
            Right = X;               // Корень в левой половине
        else {
            Left = X;                // Корень в правой половине
            FLeft = Y;               // Обновляем значение функции
        }
        N++;                         // Счётчик итераций
    }
    return X;
}